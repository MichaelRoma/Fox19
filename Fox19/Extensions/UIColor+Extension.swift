import UIKit

extension UIColor {
    static func blueForHeaderLabels() -> UIColor {
        return UIColor(red: 45/255, green: 63/255, blue: 102/255, alpha: 1)
    }
    
    static func separatorColor() -> UIColor {
        return  UIColor(red: 225/255, green: 236/255, blue: 249/255, alpha: 1)
    }
}
